package undici.auslese;

public class Zutat {
	private String zutat;
	private double preis;
	private int id;
	public String getZutat() {
		return zutat;
	}
	public void setZutat(String zutat) {
		this.zutat = zutat;
	}
	public double getPreis() {
		return preis;
	}
	public void setPreis(double preis) {
		this.preis = preis;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	
	
	

}
